import { z } from "zod";

export const galleryItemSchema = z.object({
  id: z.string(),
  image: z.string(),
  alt: z.string(),
  device: z.enum(["desktop", "laptop", "tablet", "mobile", "browser"]),
  title: z.string(),
  order: z.number(),
});

export const technologySchema = z.object({
  name: z.string(),
  icon: z.string().optional(),
  category: z.string(),
  website: z.string().url().optional().or(z.literal("")),
});

export const projectSchema = z.object({
  id: z.string(),
  title: z.string().min(1, "Title is required"),
  slug: z.string().min(1, "Slug is required"),
  subtitle: z.string().min(1, "Subtitle is required"),
  category: z.string().min(1, "Category is required"),
  type: z.enum(["Website", "Web App", "Dashboard", "Mobile App"]),
  featured: z.boolean().default(false),
  order: z.number().int().default(0),
  client: z.string().min(1, "Client name is required"),
  industry: z.string().min(1, "Industry is required"),
  shortDescription: z.string().min(1, "Short description is required"),
  fullDescription: z.string().min(1, "Full description is required"),
  technologies: z.array(technologySchema),
  thumbnail: z.string(),
  coverImage: z.string(),
  gallery: z.array(galleryItemSchema),
  colors: z.object({
    primary: z.string(),
    background: z.string(),
  }),
  liveUrl: z.string().url().optional().or(z.literal("")),
  githubUrl: z.string().url().optional().or(z.literal("")),
  completionDate: z.string(),
  testimonial: z.object({
    quote: z.string(),
    author: z.string(),
    role: z.string(),
  }).optional(),
  status: z.enum(["Draft", "Published"]),
  featuredOrder: z.number().int().optional(),
  tags: z.array(z.string()),
  services: z.array(z.string()),
  deliverables: z.array(z.string()),
  platform: z.enum(["Web", "Mobile", "Desktop", "TV"]),
  duration: z.string().optional(),
  teamSize: z.number().int().optional(),
  repositoryVisibility: z.enum(["Public", "Private"]),
  livePreviewAvailable: z.boolean(),
  caseStudyAvailable: z.boolean(),
  seoTitle: z.string().optional(),
  seoDescription: z.string().optional(),
  ogImage: z.string().optional(),
  favicon: z.string().optional(),
  thumbnailAlt: z.string(),
  createdAt: z.string(),
  updatedAt: z.string(),
});
